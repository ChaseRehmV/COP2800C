public class ArrElementDisplay {

    // method for printing all elements of an array of integers on individual lines
    public static void printElements(int[] array) {
        for (int element: array) {
            System.out.println(element);
        }
    }

    // method for printing all elements of an array of Strings on individual lines
    public static void printElements(String[] array) {
        for (String element: array) {
            System.out.println(element);
        }
    }
}
